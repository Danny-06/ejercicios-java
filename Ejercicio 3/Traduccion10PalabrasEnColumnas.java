/**
 * Muestra por pantalla 10 palabras en inglés con su correspondiente traducción al castellano
 * 
 * @autor Daniel Fabián El Belaizi
 */
public class Traduccion10PalabrasEnColumnas { // Clase principal
  public static void main(String[] args) {
    System.out.println("computer   \tordenador");
    System.out.println("keyboard   \tteclado");
    System.out.println("mouse      \tratón");
    System.out.println("device     \tdispositivo");
    System.out.println("development\tdesarrollo");
    System.out.println("number     \tnúmero");
    System.out.println("value      \tvalor");
    System.out.println("tool       \therramienta");
    System.out.println("settings   \tconfiguración");
    System.out.println("venue      \tconvento");
  }
}

